function getIconSetKey(style, animated) {
  return `${style}${animated ? "Animated" : "Static"}`;
}

export function getWeatherIcon(iconName, settings) {
  const iconSetKey = getIconSetKey(settings.iconStyle, settings.iconAnimated);

  return iconSets[iconSetKey]?.[iconName] || iconSets.filledStatic["clear-day"];
}
